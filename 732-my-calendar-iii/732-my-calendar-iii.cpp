class MyCalendarThree {
public:
    map<int,int>mp;
    MyCalendarThree() {
        
    }
    
    int book(int start, int end) {
        mp[start]++;
        mp[end]--;
        int sum=0;
        int max_sum=0;
        for(auto x:mp)
        {
            sum=max(sum,max_sum+=x.second);
        }
        return sum;
    }
};

